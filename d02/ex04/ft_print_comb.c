int		ft_putchar(char c);

void	ft_print_comb(void)
{
	int i;
	int j;
	int k;

	i = 0;
	while (i <= 9)
	{
		j = i + 1;
		while (j <= 9)
		{
			k = j + 1;
			while (k <= 9)
			{
				ft_putchar(i + 48);
				ft_putchar(j + 48);
				ft_putchar(k + 48);
				(i != 7) ? ft_putchar(',') : 1;
				(i != 7) ? ft_putchar(' ') : 1;
				k++;
			}
			j++;
		}
		i++;
	}
}
