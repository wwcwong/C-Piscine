char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a') && (str[i] <= 'z'))
		{
			str[i] -= 10;
			if (str[i] < 'a')
				str[i] += 26;
		}
		else if ((str[i] >= 'A') && (str[i] <= 'Z'))
		{
			str[i] -= 10;
			if (str[i] < 'A')
				str[i] += 26;
		}
		i++;
	}
	return (str);
}
