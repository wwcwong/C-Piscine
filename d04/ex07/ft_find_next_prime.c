#include <stdio.h>

int		ft_find_next_prime(int nb)
{
	int j;

	j = 2;
	while (nb < 2)
	{
		nb++;
	}
	while (j < nb - 1)
	{
		if (nb % j == 0)
		{
			nb++;
			j = 2;
		}
		else
		{
			j++;
		}
	}
	return (nb);
}
