int		ft_iterative_factorial(int nb)
{
	int i;

	i = nb;
	if (nb == 0 || nb == 1)
	{
		return (1);
	}
	if (nb < 0 || nb > 12)
	{
		return (0);
	}
	while (nb > 1)
	{
		i *= nb - 1;
		nb--;
	}
	return (i);
}
